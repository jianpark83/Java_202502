package ch02.sec01;

public class CharExample {

	public static void main(String[] args) {

		char c1 = 'A';
		char c2 = 65;
		//아스키코드표에 다라 결과값 표시
		char c3 = '가';
		char c4 = 44032;
		
		//ctrl+shift+방향키 아래
		System.out.println(c1);
		System.out.println(c2);
		System.out.println(c3);
		System.out.println(c4);

		//alt+방향키
	}

}
